#!/usr/bin/env python3
"""
Module: 3-to_str
This module contains a function that converts a float to a string.
"""


def to_str(n: float) -> str:
    """
    Converts a float to its string representation.
    Args:
        n (float): Float number to convert.
    Returns:
        str: String representation of n.
    """
    return str(n)
