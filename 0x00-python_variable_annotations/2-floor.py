#!/usr/bin/env python3
"""
Module: 2-floor
This module contains a function that returns the floor of a float.
"""

import math


def floor(n: float) -> int:
    """
    Returns the floor value of a float.
    Args:
    n (float): Input float number.
    Returns:
    int: Floor value of n.
    """
    return math.floor(n)
