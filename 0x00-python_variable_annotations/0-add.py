#!/usr/bin/env python3
"""
Module: 0-add
This module contains a function that adds two floats.
"""


def add(a: float, b: float) -> float:
    """
    Returns the sum of two float numbers
    Args:
        a (float): First number.
        b (float): Second number.
    Returns:
        float: Sum of a and b.
    """
    return a + b
