#!/usr/bin/env python3
"""
Module: 1-concat
This module contains a function that concatenates two strings.
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenates two strings.
    Args:
    str1 (str): First string.
    str2 (str): Second string.
    Returns:
        str: Concatenated string.
    """
    return str1 + str2
